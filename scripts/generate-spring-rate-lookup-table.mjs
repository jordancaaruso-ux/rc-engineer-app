import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function front({ a, b, branchPrimary, branchSecondary }) {
  const aExp = branchPrimary ? a - 4 : a;
  const base =
    0.81 * 84.497 * Math.exp(0.1087 * aExp) * 28.7 ** 2 / (28.7 + b) ** 2;
  return branchSecondary ? base * 0.797 : base;
}

function rear({ a, b, branchPrimary, branchSecondary }) {
  const geom = branchPrimary && branchSecondary ? 28.7 : 25.841;
  const aExp = branchPrimary && !branchSecondary ? a - 4 : a;
  const base = 84.497 * Math.exp(0.1087 * aExp) * geom ** 2 / (geom + b) ** 2;
  return !branchPrimary && branchSecondary ? base * 0.797 : base;
}

const gaps = [];
for (let i = 0; i <= 20; i++) {
  gaps.push(Math.round(i * 0.2 * 10) / 10);
}

const srsMap = {
  I: { branchPrimary: false, branchSecondary: false },
  II: { branchPrimary: true, branchSecondary: false },
};

const springs = { hard: 19, soft: 15 };

const table = { I: { front: {}, rear: {} }, II: { front: {}, rear: {} } };

for (const srsName of ["I", "II"]) {
  const br = srsMap[srsName];
  for (const side of ["front", "rear"]) {
    for (const [sn, a] of Object.entries(springs)) {
      const row = {};
      for (const b of gaps) {
        const v =
          side === "front"
            ? front({ a, b, branchPrimary: br.branchPrimary, branchSecondary: br.branchSecondary })
            : rear({ a, b, branchPrimary: br.branchPrimary, branchSecondary: br.branchSecondary });
        row[b.toFixed(1)] = Number(v.toFixed(3));
      }
      table[srsName][side][sn] = row;
    }
  }
}

const outPath = path.join(__dirname, "../src/lib/setupCalculations/springRateLookupTable.ts");
const body = `/**
 * Authoritative spring rate lookup (gf/mm) for derived front/rear spring rate.
 *
 * Grid: SRS arrangement (I | II) × side (front | rear) × spring hardness (hard | soft) × effective spring gap (mm).
 * Gap keys are 0.2 mm steps from ${gaps[0]} to ${gaps[gaps.length - 1]} (string keys, one decimal).
 *
 * Source of numeric grid: generated by scripts/generate-spring-rate-lookup-table.mjs from the legacy PDF-port
 * equations with SRS/STD/S mapping. Replace with the official Awesomatix spring-rate table values when transcribed.
 */
export const SPRING_RATE_GAP_STEP_MM = 0.2 as const;
export const SPRING_RATE_GAP_MIN_MM = ${gaps[0]} as const;
export const SPRING_RATE_GAP_MAX_MM = ${gaps[gaps.length - 1]} as const;

export const SPRING_RATE_TABLE_GF_MM = ${JSON.stringify(table, null, 2)} as const;
`;

fs.writeFileSync(outPath, body, "utf8");
console.log("Wrote", outPath);
